const errors = require('./errors')

module.exports = exports = function generateC(hyperschema) {
  return {
    header: generateHeader(hyperschema),
    source: generateSource(hyperschema)
  }
}

exports.toCName = toCName
exports.structName = structName
exports.generateCMake = generateCMake
exports.resolveBase = resolveBase
exports.typeInfo = typeInfo

const TYPES = new Map([
  ['bool', { cType: 'bool', compactSuffix: 'bool' }],
  ['uint', { cType: 'uintmax_t', compactSuffix: 'uint', signed: false }],
  ['int', { cType: 'intmax_t', compactSuffix: 'int', signed: true }],
  ['uint8', { cType: 'uint8_t', compactSuffix: 'uint8', signed: false }],
  ['uint16', { cType: 'uint16_t', compactSuffix: 'uint16', signed: false }],
  ['uint32', { cType: 'uint32_t', compactSuffix: 'uint32', signed: false }],
  ['uint64', { cType: 'uint64_t', compactSuffix: 'uint64', signed: false }],
  ['int8', { cType: 'int8_t', compactSuffix: 'int8', signed: true }],
  ['int16', { cType: 'int16_t', compactSuffix: 'int16', signed: true }],
  ['int32', { cType: 'int32_t', compactSuffix: 'int32', signed: true }],
  ['int64', { cType: 'int64_t', compactSuffix: 'int64', signed: true }]
])

function typeInfo(typeName) {
  const info = TYPES.get(typeName)
  if (!info) {
    throw errors.UNSUPPORTED_TYPE(
      `Unsupported field type "${typeName}", supported: ${[...TYPES.keys()].join(', ')}`
    )
  }
  return info
}

function getStructs(hyperschema) {
  const structs = []
  for (let i = 0; i < hyperschema.schema.length; i++) {
    const fqn = hyperschema.typesByPosition.get(i)
    const type = hyperschema.resolve(fqn)
    if (type.isStruct) structs.push(type)
  }
  return structs
}

function getNamespaces(structs) {
  return [...new Set(structs.map((t) => toCName(t.namespace)))]
}

function generateCMake(hyperschema) {
  const namespaces = getNamespaces(getStructs(hyperschema))
  if (!namespaces.length) {
    throw errors.EMPTY_SCHEMA('Schema has no structs, cannot derive CMake target name')
  }
  const target = namespaces.join('_')

  return [
    '# This file is autogenerated by hyperschema-c',
    '# !!DO NOT EDIT!!',
    '',
    'cmake_minimum_required(VERSION 4.0)',
    '',
    `project(${target} C)`,
    '',
    `add_library(${target} OBJECT schema.c)`,
    '',
    'set_target_properties(',
    `  ${target}`,
    '  PROPERTIES',
    '  C_STANDARD 99',
    ')',
    '',
    `target_include_directories(${target} PUBLIC .)`,
    '',
    `target_link_libraries(${target} PUBLIC compact)`,
    ''
  ].join('\n')
}

function generateHeader(hyperschema) {
  const structs = getStructs(hyperschema)
  const namespaces = getNamespaces(structs)
  const nsPrefix = namespaces.length ? namespaces.map((ns) => ns.toUpperCase()).join('_') + '_' : ''
  const guard = nsPrefix + 'SCHEMA_H'

  const needsBool = structs.some((t) =>
    t.fields.some((f) => !f.required || resolveBase(f.type).name === 'bool')
  )

  const lines = [
    '// This file is autogenerated by hyperschema-c',
    '// !!DO NOT EDIT!!',
    `// Schema Version: ${hyperschema.version}`,
    '',
    `#ifndef ${guard}`,
    `#define ${guard}`,
    '',
    '#include <compact.h>'
  ]

  if (needsBool) lines.push('#include <stdbool.h>')

  for (const type of structs) lines.push('', ...generateStructDecl(type))

  lines.push('', `#endif // ${guard}`, '')
  return lines.join('\n')
}

function generateSource(hyperschema) {
  const structs = getStructs(hyperschema)

  const lines = [
    '// This file is autogenerated by hyperschema-c',
    '// !!DO NOT EDIT!!',
    '',
    '#include "schema.h"'
  ]

  for (const type of structs) lines.push('', ...generateStructImpl(type))

  lines.push('')
  return lines.join('\n')
}

function toCName(name) {
  // hyphens to underscores, camelCase to snake_case
  return name
    .replace(/-/g, '_')
    .replace(/([a-z])([A-Z])/g, '$1_$2')
    .toLowerCase()
}

function structName(type) {
  return toCName(type.namespace) + '_' + toCName(type.name)
}

function generateStructDecl(type) {
  const name = structName(type)
  const lines = []

  lines.push(`// ${type.fqn}`)
  lines.push(`typedef struct ${name}_s {`)

  if (type.fields.length === 0) {
    lines.push(`  char _unused; // C99 does not allow empty structs`)
  } else {
    for (const field of type.fields) {
      const cField = toCName(field.name)
      const { cType } = typeInfo(resolveBase(field.type).name)
      if (!field.required) lines.push(`  bool has_${cField};`)
      lines.push(`  ${cType} ${cField};`)
    }
  }

  lines.push(`} ${name}_t;`)
  lines.push('')
  lines.push(`int`)
  lines.push(`${name}_preencode (compact_state_t *state, const ${name}_t *value);`)
  lines.push('')
  lines.push(`int`)
  lines.push(`${name}_encode (compact_state_t *state, const ${name}_t *value);`)
  lines.push('')
  lines.push(`int`)
  lines.push(`${name}_decode (compact_state_t *state, ${name}_t *result);`)

  return lines
}

function generateStructImpl(type) {
  const name = structName(type)
  const optionals = type.fields.filter((f) => !f.required)
  const flagsPos = type.flagsPosition // -1 = no flags (all required, compact)
  const lines = []

  lines.push(...generatePreencode(name, type.fields, flagsPos, optionals))
  lines.push('')
  lines.push(...generateEncode(name, type.fields, flagsPos, optionals))
  lines.push('')
  lines.push(...generateDecode(name, type.fields, flagsPos, optionals))

  return lines
}

function generatePreencode(name, fields, flagsPos, optionals) {
  const lines = []
  lines.push(`int`)
  lines.push(`${name}_preencode (compact_state_t *state, const ${name}_t *value)`)
  lines.push(`{`)
  if (fields.length > 0) lines.push(`  int err;`)

  for (let i = 0; i < fields.length; i++) {
    const field = fields[i]
    const cField = toCName(field.name)

    if (i === flagsPos) {
      lines.push(`  uintmax_t flags = 0;`)
      for (let j = 0; j < optionals.length; j++) {
        lines.push(
          `  if (value->has_${toCName(optionals[j].name)}) flags |= ((uintmax_t)1 << ${j});`
        )
      }
      lines.push(`  if ((err = compact_preencode_uint(state, flags)) < 0) return err;`)
    }

    const { compactSuffix } = typeInfo(resolveBase(field.type).name)
    if (field.required) {
      lines.push(
        `  if ((err = compact_preencode_${compactSuffix}(state, value->${cField})) < 0) return err;`
      )
    } else {
      lines.push(`  if (value->has_${cField}) {`)
      lines.push(
        `    if ((err = compact_preencode_${compactSuffix}(state, value->${cField})) < 0) return err;`
      )
      lines.push(`  }`)
    }
  }

  lines.push(`  return 0;`)
  lines.push(`}`)
  return lines
}

function generateEncode(name, fields, flagsPos, optionals) {
  const lines = []
  lines.push(`int`)
  lines.push(`${name}_encode (compact_state_t *state, const ${name}_t *value)`)
  lines.push(`{`)
  if (fields.length > 0) lines.push(`  int err;`)

  for (let i = 0; i < fields.length; i++) {
    const field = fields[i]
    const cField = toCName(field.name)

    if (i === flagsPos) {
      lines.push(`  uintmax_t flags = 0;`)
      for (let j = 0; j < optionals.length; j++) {
        lines.push(
          `  if (value->has_${toCName(optionals[j].name)}) flags |= ((uintmax_t)1 << ${j});`
        )
      }
      lines.push(`  if ((err = compact_encode_uint(state, flags)) < 0) return err;`)
    }

    const { compactSuffix } = typeInfo(resolveBase(field.type).name)
    if (field.required) {
      lines.push(
        `  if ((err = compact_encode_${compactSuffix}(state, value->${cField})) < 0) return err;`
      )
    } else {
      lines.push(`  if (value->has_${cField}) {`)
      lines.push(
        `    if ((err = compact_encode_${compactSuffix}(state, value->${cField})) < 0) return err;`
      )
      lines.push(`  }`)
    }
  }

  lines.push(`  return 0;`)
  lines.push(`}`)
  return lines
}

function generateDecode(name, fields, flagsPos, optionals) {
  const lines = []
  lines.push(`int`)
  lines.push(`${name}_decode (compact_state_t *state, ${name}_t *result)`)
  lines.push(`{`)
  if (fields.length > 0) lines.push(`  int err;`)

  for (let i = 0; i < fields.length; i++) {
    const field = fields[i]
    const cField = toCName(field.name)

    if (i === flagsPos) {
      lines.push(`  uintmax_t flags = 0;`)
      lines.push(`  if ((err = compact_decode_uint(state, &flags)) < 0) return err;`)
      for (let j = 0; j < optionals.length; j++) {
        lines.push(
          `  result->has_${toCName(optionals[j].name)} = (flags & ((uintmax_t)1 << ${j})) != 0;`
        )
      }
    }

    const { compactSuffix } = typeInfo(resolveBase(field.type).name)
    if (field.required) {
      lines.push(
        `  if ((err = compact_decode_${compactSuffix}(state, &result->${cField})) < 0) return err;`
      )
    } else {
      lines.push(`  if (result->has_${cField}) {`)
      lines.push(
        `    if ((err = compact_decode_${compactSuffix}(state, &result->${cField})) < 0) return err;`
      )
      lines.push(`  }`)
    }
  }

  lines.push(`  return 0;`)
  lines.push(`}`)
  return lines
}

function resolveBase(type) {
  if (!type) {
    throw errors.UNRESOLVED_TYPE('Field has no resolved type, schema may be incomplete')
  }
  if (type.isAlias) return resolveBase(type.type)
  return type
}
